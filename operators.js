// Two let variables num1 and num2

let num1 = 10;
let num2 = 5;

// Perform basic mathematical operations on these variables

// addition
console.log('num1 + num2= ', num1 + num2);  // 15

// subtraction
console.log('num1 - num2= ', num1 - num2);  // 5

// multiplication
console.log('num1 * num2= ', num1 * num2);  // 50

// division
console.log('num1 / num2= ', num1 / num2);  // 2

// modulus
console.log('num1 % num2= ', num1 % num2);   // 0

// Use the increment (++) and decrement (--) operators

// increment
console.log('++num1 = ', ++num1); // num1 is now 11
console.log('num1++ = ', num1++); // prints 11 and then it increased to 12
console.log('num1 = ', num1);     // 12

console.log('++num2 = ', ++num2); // num2 is now 6
console.log('num2++ = ', num2++); // prints 6 and then it increased to 7
console.log('num2 = ', num2);     // 7

// decrement
console.log('--x = ', --num2); // num1 is now 11
console.log('num1-- = ', num1--); // prints 11 and then it decreased to 10
console.log('num1 = ', num1);     // 10

console.log('--x = ', --num1); // num2 is now 6
console.log('num2-- = ', num2--); // prints 6 and then it decreased to 5
console.log('num2 = ', num2);     // 5

